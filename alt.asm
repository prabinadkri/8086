MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	MOV AH,0AH
	LEA DX,INPUT
	INT 21H
	MOV CX,LEN
	MOV BL,0
	MOV BH,'a'-'A'
	MOV DI,0
LOO:CMP BL,0
	JE LO
	CMP S[DI],'a'
	JB NEXT
	CMP S[DI],'z'
	JA NEXT
	SUB S[DI],BH
	JMP NEXT
LO: CMP S[DI],'Z'
	JA NEXT
	CMP S[DI],'A'
	JB NEXT
	ADD S[DI],BH
NEXT:XOR BL,0FFH
	INC DI
	LOOP LOO
	MOV AH,00H
	MOV AL,03H
	INT 10H
	MOV AH,09H
	LEA DX,S
	INT 21H
	MOV AX,4C00H
	INT 21H
MAIN ENDP
