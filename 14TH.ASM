TITLE CHANGE TO UPPERCASE
	.MODEL SMALL
	.DATA
ROW DB 12
COL DB 40
lo  DB 0
INP LABEL BYTE
MAX DB 20
LEN DB ?
S DB 20 DUP('$')
	.STACK
	.CODE
MAIN PROC FAR 
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH
	LEA DX, INP
	INT 21H
	
	
	MOV CX,LEN
	MOV AX,'a'-'A'
RE: MOV BX,CX
	CMP S[BX-1],'a'
	JB SKIP
	CMP S[BX-1],'z'
	JA SKIP
	SUB S[BX-1],AX
SKIP:LOOP RE

	MOV CL,2
	MOV AH,00
	MOV AL,LEN
	DIV CL
	SUB COL,AL
	
	
		
		MOV AH,00H
		MOV AL,03H
		INT 10H
		MOV AH,06H
		MOV AL,16
		MOV BH,0F1H
		MOV CX,020AH
		MOV DX,1646H
		INT 10H
		
		
	
	lea DI,S
	mov dx,0
	DISP:
		push dx
	CALL SETCUR
		pop dx
		MOV AH,0AH
		MOV AL,[DI]
		MOV BH,00
		MOV CX,01
		
		INT 10H
		ADD COL,2
		CMP COL,70
		JB SKI
		INC ROW
		MOV COL,10
SKI:	INC DI
		inc dx
		CMP dx,LEN
		JB DISP
	
	
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
SETCUR PROC NEAR
	MOV AH,02H
		MOV BH,00
		MOV DH,ROW
		MOV DL,COL
		INT 10H
		RET
SETCUR ENDP
END MAIN