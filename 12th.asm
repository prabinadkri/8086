TITLE COUNT VOWEL
	.MODEL SMALL
	.DATA
COU DB 0
VOW DB 'A','E','I','O','U','a','e','i','o','u'
INP LABEL BYTE
MAX DB 20
LEN   DB ?
S   DB 20 DUP('$')
	.STACK 
	.CODE
MAIN PROC FAR
		MOV AX,@DATA
		MOV DS,AX
	
		MOV AH,0AH
		LEA DX ,INP
		INT 21H
	
	
		MOV COU,0
	MOV CX,LEN
	
RE: MOV DI,0
	MOV BX,CX
NEX:MOV DL,VOW[DI]
	CMP S[BX-1],DL
	JZ SK
	INC DI
	CMP DI,10
	JB NEX
	JMP SKIP
SK:	INC COU
SKIP:LOOP RE
	;DISPLAY
	MOV AH,02H
	MOV DL,0DH
	INT 21H
	MOV AH,02H
	MOV DL,0AH
	INT 21H
	
	MOV AH,00
	MOV AL,COU
	
	MOV CX,00H
	MOV BX,10
L1:	MOV DX,0
	DIV BX
	ADD DX,30H
	PUSH DX
	INC CX
	CMP AX,0
	JA L1
		
	MOV AH,02H
DISP:POP DX
	INT 21H
	LOOP DISP
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN
	
	
	