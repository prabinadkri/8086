TITLE CHANGE TO UPPERCASE
	.MODEL SMALL
	.DATA
VOW DB 'a','e','i','o','u'
INP LABEL BYTE
MAX DB 20
LEN DB ?
S DB 20 DUP('$')

	.STACK
	.CODE
MAIN PROC FAR 
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH
	LEA DX, INP
	INT 21H
	
	
	MOV CX,LEN
	MOV AX,'a'-'A'
RE: MOV DI,0
	MOV BX,CX
NEX:MOV DL,VOW[DI]
	CMP S[BX-1],DL
	JZ SK
	INC DI
	CMP DI,5
	JB NEX
	JMP SKIP
SK:	SUB S[BX-1],AX
SKIP:LOOP RE

	MOV AH,02H
	MOV DL,0DH
	INT 21H
	MOV AH,02H
	MOV DL,0AH
	INT 21H
	MOV AH,09H
	LEA DX,S
	INT 21H
	MOV AX, 4C00H
	INT 21H
MAIN ENDP
END MAIN