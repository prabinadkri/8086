TITLE DISPLAY ASCII
	.MODEL SMALL
	.DATA
as 	DB 32
ROW DB 5
COL DB 10
	.STACK
	.CODE
MAIN PROC FAR
		MOV AX,@DATA
		MOV DS,AX
	
		MOV AH,00H
		MOV AL,03H
		INT 10H
		MOV AH,06H
		MOV AL,16
		MOV BH,0F1H
		MOV CX,050AH
		MOV DX,1446H
		INT 10H
		MOV CX,96
		
		
		
DISP: 	CALL SETCUR
		
		MOV AH,0AH
		MOV AL,as
		MOV BH,00
		MOV CX,01
		
		INT 10H
		ADD COL,2
		CMP COL,70
		JB SKIP
		INC ROW
		MOV COL,10
SKIP:	INC as
		
		CMP as,127
		JBE DISP
		
		MOV AX,4C00H
		INT 21H
		
MAIN ENDP
; set cursor
SETCUR PROC NEAR
	MOV AH,02H
		MOV BH,00
		MOV DH,ROW
		MOV DL,COL
		INT 10H
		RET
SETCUR ENDP
END MAIN
	