TITLE Program to find sum of squares of first n odd natural numbers
 .MODEL
 .DATA
 SH DW 0
 SL DW 0
 INP LABEL BYTE
 MAX DB 3
 LEN DB ?
 S DB 4 DUP('$')
 .STACK
 .CODE
 MAIN PROC FAR
 MOV AX,@DATA
 MOV DS,AX
 MOV AH,0AH
 LEA DX,INP
 INT 21H
 
 MOV AX,00
 MOV CX,LEN
 MOV DL,10
 NUM:MOV BX,LEN
 SUB BX,CX

 SUB S[BX],30H
 MUL DL
 ADD AX,S[BX]
 LOOP NUM
 MOV BX,1
 MOV CX,AX
LOOPNEXT: MOV AX,BX
MUL AX
ADD SL,AX
JNC NORM
ADD SH,01
NORM:ADD SH,DX


ADD BX,2
LOOP LOOPNEXT

;MOV AH,02H
;MOV DL,0DH
;INT 21H

MOV AH,02H
MOV DL,0AH
INT 21H
mov dx,0
MOV AX,SL
MOV CX,0
MOV BX,10
NEP:DIV BX
inc cx
PUSH DX
CMP AX,0
JA NEP
MOV DX,0
MOV AX,SH
X:DIV BX
PUSH DX
inc cx
CMP AX,0
JA X
MOV AH,02H
DISP:POP DX
INT 21H
LOOP DISP
MOV AX,4C00H
INT 21H
MAIN ENDP
END MAIN